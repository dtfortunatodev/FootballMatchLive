package com.formobile.projectlivestream.configs;

/**
 * A entity for other sport
 */
public class OtherSportJSONEntity{
    private String namepackage;
    private String resDrawableId;
    private String name;
    private String urlInstall;

    public String getNamepackage() {
        return namepackage;
    }

    public void setNamepackage(String namepackage) {
        this.namepackage = namepackage;
    }

    public String getResDrawableId() {
        return resDrawableId;
    }

    public void setResDrawableId(String resDrawableId) {
        this.resDrawableId = resDrawableId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrlInstall() {
        return urlInstall;
    }

    public void setUrlInstall(String urlInstall) {
        this.urlInstall = urlInstall;
    }
}
